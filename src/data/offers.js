function buildDates(startMonthOffset, count = 4) {
  const statuses = ['available', 'available', 'limited', 'soldout'];
  const dates = [];
  const base = new Date();
  base.setDate(1);
  for (let i = 0; i < count; i++) {
    const d = new Date(base);
    d.setMonth(d.getMonth() + startMonthOffset + i);
    dates.push({ date: d.toISOString().slice(0, 10), status: statuses[i % statuses.length] });
  }
  return dates;
}

